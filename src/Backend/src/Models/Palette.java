package Models;

public class Palette {
    int myRed;
    int myGreen;
    int myBlue;

    public Palette(int r, int g, int b) {
        myRed = r;
        myGreen = g;
        myBlue = b;
    }

    public int getMyRed() {
        return myRed;
    }

    public int getMyGreen() {
        return myGreen;
    }

    public int getMyBlue() {
        return myBlue;
    }
}
